/*
--> Assignment opeator : they are use to assign the value to the variables.
    = is called as assignment operator.

    We can use precedence table which tells the precedence of different operators.

    Always use grouping operator while working with expressions.
    Grouping operator () has the highest precedence.

*/

#include<iostream>
using namespace std ; 

int main(){
    int a = 5 ; 
    a += 10 ; 
    a -= 10 ; 
    a /= 10 ; 
    a %= 10 ; 

    return 0 ; 
}