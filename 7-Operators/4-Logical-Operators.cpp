/*
--> What are logical operators ? 
    These operators are used to make a condition logically.

    Logical Operator : && , || , !
    They are used with conditions.

    AND (&&) : If all the values are true than output will come as true else output will 
    come as false.

    true + true = true
    true + false = false 
    false + true = false 
    false + false = false

    OR (||) : If any one value is true than output will come as true else output will 
    come as false.

    true + true = true
    true + false = true
    false + true = true
    false + false = false

    NOT (!) : It makes true as false and false as true.

    false = true
    true = false

*/

